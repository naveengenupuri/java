public class Ball
{
    private int xcoordinate,ycoordinate;
    public Ball(int xvalue,int yvalue)
    {
        this.xcoordinate=xvalue;
        this.ycoordinate=yvalue;
    }
    public int getXcoordinate()
    {
        return xcoordinate;
    }

    public int getYcoordinate()
    {
        return ycoordinate;
    }

}